package com.antaranga.jsf.beans;


public class TableItem {

    private String key;
    private String value;
    
    public TableItem() {
        
    }
    
    public String getKey() {
        return this.key;
    }
    
    public void setKey(String key) {
        this.key = key;
    }
    
    public String getValue() {
        return this.value;
    }
    
    public void setValue(String value) {
        this.value = value;
    }
    
}
